#!/usr/bin/env bash
# hermes-publish-gate: 12-check quality gate before making a repo public.
# Usage: bash publish-gate.sh /path/to/repo

set -euo pipefail

REPO="${1:-.}"
cd "$REPO"

REPO_NAME=$(basename "$(git rev-parse --show-toplevel 2>/dev/null || pwd)")
PASS=0
FAIL=0
WARN=0

# ── Configurable ──────────────────────────────────────────────────────
# API key patterns to scan for
SECRET_PATTERNS='(sk-ant-[a-zA-Z0-9_-]{20,}|sk-or-v1-[a-zA-Z0-9_-]{20,}|nvapi-[a-zA-Z0-9_-]{20,}|AKIA[0-9A-Z]{16}|ghp_[a-zA-Z0-9]{36}|gho_[a-zA-Z0-9]{36}|glpat-[a-zA-Z0-9_-]{20,})'
# PII patterns
PII_PATTERNS='(\b[0-9]{3}-[0-9]{2}-[0-9]{4}\b)'
# AI attribution / tell patterns
AI_ATTRIBUTION_PATTERNS='(co-authored-by.*(claude|copilot|gpt|chatgpt)|generated by (ai|claude|chatgpt|copilot)|ai-generated|created with (claude|chatgpt|copilot)|written by (claude|chatgpt|ai)|as an ai language model|i am an ai|i'\''m an ai)'
# Allowed git author name fragments. Override with ALLOWED_AUTHORS_CSV.
IFS=',' read -r -a ALLOWED_AUTHORS <<< "${ALLOWED_AUTHORS_CSV:-}"
ALLOWLIST_FILE="${ALLOWLIST_FILE:-.security-allowlist}"

_pass() { echo "  $1. $2  ✓ PASS"; ((++PASS)); }
_fail() { echo "  $1. $2  ✗ FAIL — $3"; ((++FAIL)); }
_warn() { echo "  $1. $2  ⚠ WARN — $3"; ((++WARN)); }

_filter_allowlist() {
  local allow_tmp
  allow_tmp=$(mktemp)
  if [ -f "$ALLOWLIST_FILE" ]; then
    grep -Ev '^[[:space:]]*(#|$)' "$ALLOWLIST_FILE" > "$allow_tmp" || true
  fi
  if [ -s "$allow_tmp" ]; then
    grep -Ev -f "$allow_tmp" || true
  else
    cat
  fi
  rm -f "$allow_tmp"
}

echo ""
echo "╔══════════════════════════════════════╗"
echo "║     Publish Gate — $REPO_NAME"
echo "╚══════════════════════════════════════╝"
echo ""

# 1. Secret scan (full history)
SECRET_HISTORY_HITS=$({ git log -p --all 2>/dev/null || true; } | { grep -E "$SECRET_PATTERNS" 2>/dev/null || true; } | _filter_allowlist)
if [ -n "$SECRET_HISTORY_HITS" ]; then
  _fail 1 "Secret scan (history)" "API key pattern found in commit history"
else
  _pass 1 "Secret scan (history)"
fi

# 2. Secret scan (current files)
SECRET_FILE_HITS=$({ grep -rE "$SECRET_PATTERNS" --include="*.py" --include="*.js" --include="*.ts" --include="*.sh" --include="*.yaml" --include="*.yml" --include="*.json" --include="*.env" --include="*.cfg" --include="*.ini" . 2>/dev/null || true; } | { grep -v "\.git/" || true; } | _filter_allowlist)
if [ -n "$SECRET_FILE_HITS" ]; then
  _fail 2 "Secret scan (files)" "API key pattern in tracked files"
else
  _pass 2 "Secret scan (files)"
fi

# 3. PII patterns
PII_HITS=$({ grep -rE "$PII_PATTERNS" --include="*.py" --include="*.js" --include="*.ts" --include="*.md" --include="*.txt" . 2>/dev/null || true; } | { grep -v "\.git/" || true; } | _filter_allowlist)
if [ -n "$PII_HITS" ]; then
  _fail 3 "PII patterns" "SSN or similar pattern found"
else
  _pass 3 "PII patterns"
fi

# 4. Git author PII
AUTHORS=$(git log --format='%an <%ae>' --all 2>/dev/null | sort -u || true)
SUSPICIOUS_AUTHORS=$(echo "$AUTHORS" | grep -viE '(bot|ci|github|noreply|users\.noreply)' | head -5 || true)
if [ -n "$SUSPICIOUS_AUTHORS" ] && [ ${#ALLOWED_AUTHORS[@]} -gt 0 ]; then
  HAS_UNKNOWN=false
  while IFS= read -r author; do
    FOUND=false
    for allowed in "${ALLOWED_AUTHORS[@]}"; do
      if echo "$author" | grep -qi "$allowed"; then FOUND=true; break; fi
    done
    $FOUND || HAS_UNKNOWN=true
  done <<< "$SUSPICIOUS_AUTHORS"
  $HAS_UNKNOWN && _fail 4 "Git author PII" "unknown author in history: check with 'git log --format=\"%an %ae\" | sort -u'" || _pass 4 "Git author PII"
else
  _warn 4 "Git author PII" "review authors: $(echo "$SUSPICIOUS_AUTHORS" | head -3 | tr '\n' ', ')"
fi

# 5. AI attribution
AI_HISTORY_HITS=$({ git log --all --format='%B' 2>/dev/null || true; } | { grep -iE "$AI_ATTRIBUTION_PATTERNS" 2>/dev/null || true; } | _filter_allowlist)
if [ -n "$AI_HISTORY_HITS" ]; then
  _fail 5 "AI attribution" "found in commit messages"
else
  AI_FILE_HITS=$({ grep -rliE "$AI_ATTRIBUTION_PATTERNS" --include="*.py" --include="*.js" --include="*.ts" --include="*.sh" --include="*.yaml" --include="*.yml" --include="*.json" . 2>/dev/null || true; } | { grep -vE '(^|/)\.git/|(^|/)publish-gate\.sh$' || true; } | _filter_allowlist | head -1)
  if [ -n "$AI_FILE_HITS" ]; then
  _fail 5 "AI attribution" "found in source files"
  else
  _pass 5 "AI attribution"
  fi
fi

# 6. Private keys/certs
if git ls-files 2>/dev/null | grep -qE '\.(pem|key|p12|pfx|jks)$'; then
  _fail 6 "Private keys/certs" "key/cert files tracked in git"
else
  PRIVATE_KEY_HITS=$({ grep -rl -e '-----BEGIN.*PRIVATE KEY-----' --include="*.py" --include="*.js" --include="*.yaml" --include="*.json" --include="*.sh" . 2>/dev/null || true; } | { grep -vE '(^|/)\.git/|(^|/)(publish-gate|block-secrets)\.sh$' || true; } | _filter_allowlist | head -1)
  if [ -n "$PRIVATE_KEY_HITS" ]; then
  _fail 6 "Private keys/certs" "private key content in source files"
  else
  _pass 6 "Private keys/certs"
  fi
fi

# 7. Build artifacts
ARTIFACTS=""
git ls-files 2>/dev/null | grep -qE '__pycache__/' && ARTIFACTS="__pycache__ "
git ls-files 2>/dev/null | grep -qE 'node_modules/' && ARTIFACTS="${ARTIFACTS}node_modules "
git ls-files 2>/dev/null | grep -qE '\.pyc$' && ARTIFACTS="${ARTIFACTS}.pyc "
if [ -n "$ARTIFACTS" ]; then
  _warn 7 "Build artifacts" "${ARTIFACTS}tracked (add to .gitignore)"
else
  _pass 7 "Build artifacts"
fi

# 8. .env tracked
if git ls-files 2>/dev/null | grep -qE '^\.env$|/\.env$'; then
  _fail 8 ".env tracked" ".env file is in git — remove it"
else
  _pass 8 ".env tracked"
fi

# 9. README exists
if [ -f "README.md" ] || [ -f "README.rst" ] || [ -f "README" ]; then
  LINES=$(wc -l < README.md 2>/dev/null || echo 0)
  if [ "$LINES" -lt 5 ]; then
    _warn 9 "README exists" "README is very short ($LINES lines)"
  else
    _pass 9 "README exists"
  fi
else
  _fail 9 "README exists" "no README found"
fi

# 10. LICENSE exists
if [ -f "LICENSE" ] || [ -f "LICENSE.md" ] || [ -f "COPYING" ]; then
  _pass 10 "LICENSE exists"
else
  _fail 10 "LICENSE exists" "no license = all rights reserved"
fi

# 11. Dependency CVEs
if command -v pip-audit >/dev/null 2>&1 && [ -f "requirements.txt" ]; then
  CVE_COUNT=$(pip-audit -r requirements.txt --format json 2>/dev/null | python3 -c "import json,sys; print(len(json.load(sys.stdin).get('dependencies',[])))" 2>/dev/null || echo 0)
  [ "$CVE_COUNT" -gt 0 ] && _warn 11 "Dependency CVEs" "$CVE_COUNT findings from pip-audit" || _pass 11 "Dependency CVEs"
elif command -v npm >/dev/null 2>&1 && [ -f "package.json" ]; then
  CVE_COUNT=$(npm audit --json 2>/dev/null | python3 -c "import json,sys; d=json.load(sys.stdin); print(d.get('metadata',{}).get('vulnerabilities',{}).get('total',0))" 2>/dev/null || echo 0)
  [ "$CVE_COUNT" -gt 0 ] && _warn 11 "Dependency CVEs" "$CVE_COUNT findings from npm audit" || _pass 11 "Dependency CVEs"
else
  _warn 11 "Dependency CVEs" "no pip-audit or npm audit available — skipped"
fi

# 12. Debug code
DEBUG_COUNT=$({ grep -rcE '(console\.log|print\(|breakpoint\(\)|import pdb|debugger;)' --include="*.py" --include="*.js" --include="*.ts" . 2>/dev/null || true; } | awk -F: '{s+=$2} END {print s+0}')
if [ "$DEBUG_COUNT" -gt 0 ]; then
  _warn 12 "Debug code" "$DEBUG_COUNT debug statements found"
else
  _pass 12 "Debug code"
fi

# ── Summary ───────────────────────────────────────────────────────────
echo ""
echo "────────────────────────────────────────"
if [ "$FAIL" -gt 0 ]; then
  echo "Result: FAIL ($FAIL critical/high, $WARN warnings, $PASS passed)"
  echo "Fix the FAIL items before making this repo public."
  exit 1
elif [ "$WARN" -gt 0 ]; then
  echo "Result: PASS with warnings ($WARN warnings, $PASS passed)"
  echo "Review warnings — they're advisory but worth fixing."
  exit 0
else
  echo "Result: PASS ($PASS/12 checks passed)"
  echo "This repo is ready to go public."
  exit 0
fi
